const brun = document.querySelector("#brunch");
const ric = document.querySelector("#rice");
const nood = document.querySelector("#noodle");
const buff = document.querySelector("#buffet");
const oth = document.querySelector("#other");
const button = document.querySelector("#btn");
const resp = document.querySelector("#response");

button.addEventListener("click" , function(i){
    let chArr = [];
    menuList.forEach((i)=>{
        i.filter = false;
    });
    if(brun.checked) {
        menuList.forEach((x)=>{
            x.category.forEach((y)=>{
                if(y==="brunch"){
                    x.filter = true;
                }
            });
        });
    }

    if(ric.checked) {
        menuList.forEach((x)=>{
            x.category.forEach((y)=>{
                if(y==="rice"){
                    x.filter = true;
                }
            });
        });
    }

    if(nood.checked) {
        menuList.forEach((x)=>{
            x.category.forEach((y)=>{
                if(y==="noodle"){
                    x.filter = true;
                }
            });
        });
    }

    if(buff.checked) {
        menuList.forEach((x)=>{
            x.category.forEach((y)=>{
                if(y==="buffet"){
                    x.filter = true;
                }
            });
        });
    }

    if(oth.checked) {
        menuList.forEach((x)=>{
            x.category.forEach((y)=>{
                if(y==="others"){
                    x.filter = true;
                }
            });
        });
    }

    menuList.forEach((i)=>{
        if(i.filter == true){
            chArr.push(i.name);
        }
    });

    if(chArr.length !== 0){
        const random = Math.floor(Math.random() * chArr.length);
        resp.innerText = "吃爆 " + chArr[random] + "!!!!!!!!";
    } else {
        resp.innerText = "做仙!!!!!!";
    }
});

